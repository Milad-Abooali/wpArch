<div style="text-align: left;">
	تحریریه معماری آرل</div>
<p>
	پروژه ساختمان آکادمی هنر یکی از فرصت ها برای بازگرداندن این منطقه به شکوه سابق خود است. در این پروژه، علاوه بر ایجاد یک جسم جدید، یکی از مشکلات اصلی ما در حفظ تهویه فضا و فضای سبز طبیعی در شهر می باشد. بنابراین، ساختمان ما یک جسم مستقل نیست، اما جسمی محاط شده توسط طبیعت است، تا اینکه خودش چشم انداز باشد. ما نه تنها میخواهیم از داخل ساختمان استفاده کنیم بلکه تمام سطوح آن را نیز نیاز داریم.<br>
	این شهر مکان های معمولی ساکنین برای استراحت و پیاده روی را از دست نمی دهد.</p>
<h2>
	روند طراحی و کانسپت پروژه ساختمان آکادمی هنر</h2>
<p>
	<strong>« به همه ی گل ها اجازه دهید که شکوفه دهند و تمام هنرها را توسعه دهند. »</strong><br>
	مونپارنس منطقه ای در جنوب پاریس، در سمت چپ seine است. در اوایل قرن بیستم، محله مونپارنس محبوب شد، اینجا همه روشنفکران خلاق شروع به گرد آمدن در کافه ها و رستوران های شگفت انگیز کردند. بسیاری از این مکان ها هنوز باز هستند و فضای شیوه زندگی بوهمیک را حفظ می کنند.<br>
	ساختمان <a href="https://www.arel.ir/fa/News-Category-638.html">دانشکده هنر</a> یک تپه سبز است که به تدریج از زمین بیرون آمده است، و این باعث می شود که نه تنها در پارک اطراف ساختمان، بلکه روی سطح ساختمان نیز راه برود. از تپه ها به مقاطع مثلثی که شبیه به گلبرگ های یک گل شکفته هستند بالا می رویم.<br>
	از طرف "گلبرگها"، پنجره های شیشه ای عمودی قرار داده شده اند که نور زیادی را به ساختمان می افزایند. سطح گلبرگ های بزرگ طراحی شده به عنوان میدان تجمعی که در آن فضای نشستن قرار داده شده اند، در گلبرگ های متوسط و کوچک تخت های گلی هستند.<br>
	در مرکز تپه نیز یک حوضچه قرار داده شده است. کف حوضچه شفاف است، که باعث می شود که نور خورشید در قسمت مرکزی ساختمان، جایی که سالن مرکزی واقع شده است، ایجاد شود. در اطراف حوضچه زیر سکو، ( که در زمان های عادی یک کافه در فضای باز است ) ، فضای سِن واقع شده است، که می تواند در بالای آبشار نصب شود.<br>
	صحنه و فضای نشستن هایی که برای تماشاگران در گلبرگ های بزرگ ایجاد می شود امکان برگزاری کنسرت ها، جشنواره ها و دیگر رویدادهای عمومی را فراهم می کند. همچنین ممکن است چادر را روی صحنه و فضای نشستن تماشاگران قرار دهیم.</p>
<p>
	تمام اتاق های ساختمان در اطراف سالن مرکزی گروه بندی می شوند. برای اتصال آشپزخانه و کافه در فضای باز راه پله ای همراه با یک آسانسور در داخل ساختمان طراحی شده، که در حجم شیشه ای استوانه ای محصور شده است، هنرمندانی که روی صحنه اجرا می کنند روی این فضاهای ارتباطی حرکت می کنند.<br>
	ما معتقدیم که ساختمان جدید نه تنها برای کسانی که در آن درس بخواند، بلکه برای همه ساکنان مونپارنس نیز جالب خواهد بود.<br>
	(طراحی شده و تکمیل شده در سال 2017)</p>
