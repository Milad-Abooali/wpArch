<h2>
	اطلاعات بیشتر از پروژه آکادمی هنر</h2>
<p>
	• فونداسیون شامل یک اسلب بتن مسلح یکپارچه با ضخامت 300 میلی متر است.<br>
	• گنبد شامل یک غشاء بتن مسلح با دیواره نازک است.<br>
	• اسکلت شامل ستون های بتن مسلح است.<br>
	• پنجره ها از شیشه های رنگی، چوبی و فلزی دو جداره هستند.<br>
	• پوشش سقف معکوس کار شده ( ضخامت خاک همراه با گیاهان سبز 600 میلی متر است )<br>
	• حیات گیاهی شامل درختچه ها ، گل های چند ساله یا جوان و هم چنین پوشش چمن است.<br>
	• مساحت کلاس تئاتر 215 متر مربع است.<br>
	• مساحت کلاس مجسمه سازی و کلاس هنر 152 متر مربع است.<br>
	• فضای مفید 614 متر مربع است.<br>
	• درصد استفاده از فضا 0.68 است.<br>
	• فضای تراس برای کافه تابستانی 98 متر مربع است.<br>
	• تعداد صندلی های داخل سالن اجتماعات 600 عدد است.<br>
	• سطح فضای سبز 2210 متر مربع است.<br>
