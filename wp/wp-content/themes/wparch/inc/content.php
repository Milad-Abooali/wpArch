<h2>
	اطلاعات فنی</h2>
<p>
	اکولوژی - یکی از اجزای مهم امکانات پیش بینی شده در یک شهر پرجمعیت است.<br>
	ساختمان آکادمی در قالب گنبد تپه ای با سقف معکوس سبز طراحی شده است، که به شما اجازه می دهد تا تعادل بین زیست کره (biosphere) و زیستگاه انسانی تازه ایجاد شده را حفظ کنید.</p>
<p>
	<strong>این فرم و پوشش بدون هزینه اضافی موارد زیر را موجب می شود :</strong></p>
<div style="margin-right: 40px;">
	- کاهش دمای هوا در شهرها در تابستان (به طور متوسط تا 10 درجه سانتیگراد)؛<br>
	- از بین بردن مواد شیمیایی خطرناک برای سلامت انسان و ترکیب و مواد تبخیری حاصل از مصالح قیری بام؛<br>
	- کاهش آلودگی هوا (گیاهان روی سقف می توانند تا 50٪ از گرد و غبار عبوری از سطح بام را جذب کنند و غلظت میکرو ارگانیسم های مضر را کاهش دهند)<br>
	&nbsp;</div>
<p>
	<strong>تضمین تامین اکسیژن (زمین پوشیده از چمن 150 متر مربعی، اکسیژن سالانه را اختصاص می دهد که برای تنفس صد نفر کفایت میکند)؛</strong></p>
<div style="margin-right: 40px;">
	- کاهش نویز پس زمینه از 2 تا 10 دسی بل؛<br>
	- افزایش رطوبت هوا در شهرها به دلیل تبخیر آهسته رطوبت از خاک، که تاثیر منفی بر سلامت انسان دارد؛<br>
	- جلوگیری از گسترش سریع آتش در هنگام آتش سوزی.<br>
	&nbsp;</div>
<p>
	<strong>هنگام انتخاب شکل ساختمان، (سازمان داخلی آن)، سیستم های انتخابی مهندسی پشتیبانی و مواد، یکی از اهداف مهم کاهش مصرف انرژی بود.</strong></p>
<div style="margin-right: 40px;">
	- سهم قابل توجهی از صرفه جویی در انرژی با عایق حرارتی خوب به این روش به دست خواهد آمد که خاک گرما را خیلی ضعیف عبور می دهد و آن را به خوبی ذخیره می کند، که می تواند هزینه گرمایش و تهویه مطبوع را کاهش دهد. چنین خانه ای با درجه حرارت پایدار در داخل تامین می شود.<br>
	- این نوع خانه مخافظت بهتری از افزایش سر و صدای خارجی در شرایط افزایش جریان ترافیک ایجاد می کند که هزینه های عایق صدا را کاهش می دهد.<br>
	- در این مورد، سطح کمتری وجود دارد، بنابراين، تعمیر بخش بیرونی خانه، در مقایسه با سایر انواع خانه ها کمتر است؛<br>
	- در یک خانه از این نوع تابستان همیشه خنک تر از یک خانه عادی است؛<br>
	- دوره ساخت و ساز خانه کوتاه شده است، چرا که بخش نمای پرکار و کار بام، دیگر نیست، به این ترتیب مصالح مورد تیاز آنها نیز لازم نیست؛<br>
	- استفاده از پنجره های دو جداره با پوشش مخصوص در شیشه های رنگی از ورود گرمای بیش از حد و صدا در داخل جلوگیری می کند.<br>
	- گردش آب در مخزن همچنین در کاهش دمای داخلی هوا در طول فصل هایی با دمای بالا تاثیر می گذارد.</div>
